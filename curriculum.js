export function getLessonById(id) {
  return curriculum.lessons.find(l => l.id === id) || null;
}

export function getLevelLessons(level) {
  return curriculum.lessons.filter(l => l.level === level);
}

export function getLevelData(level) {
  return curriculum.levels.find(l => l.id === level) || null;
}

export function getTotalLessons() {
  return curriculum.lessons.filter(l => !l.isQuiz).length;
}

export function getNextLesson(currentId) {
  const idx = curriculum.lessons.findIndex(l => l.id === currentId);
  return idx >= 0 && idx < curriculum.lessons.length - 1 ? curriculum.lessons[idx + 1] : null;
}

export function getPrevLesson(currentId) {
  const idx = curriculum.lessons.findIndex(l => l.id === currentId);
  return idx > 0 ? curriculum.lessons[idx - 1] : null;
}
